# Printing multiple lines of code with "\n"
# "\n" brings the code to the next line on output
print("Hickory Dickory Dock! \nThe mouse went up the clock")

# "\t" inserts an indentation to the code on output
print("Hickory Dickory Dock! \n\tThe mouse went up the clock")

# inserting a double quote in a string using a backslash(\)
print("here is a double quote \"")

# a case where we will need double backslashes(\\)
print("here is the \\news")